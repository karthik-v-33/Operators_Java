package com.GitHub_Updates;
//Operators are symbols that performs a particular task and 
//based on the type of operation operators are classified.

public class Operators_in_Java_Arithmetic_Operators {
/*Arithmetic operators
 *Arithmetic operators are used to perform common mathematical operations.
 1) +
 2) -
 3) *
 4) /
 5) % (Modulo Operation (Remainder after division))
*/
	public static void main(String[] args) {
		  
	    // declare variables
	    int a = 12, b = 5;

	    // addition operator
	    System.out.println("a + b = " + (a + b));

	    // subtraction operator
	    System.out.println("a - b = " + (a - b));

	    // multiplication operator
	    System.out.println("a * b = " + (a * b));

	    // division operator
	    System.out.println("a / b = " + (a / b));

	    // modulo operator
	    System.out.println("a % b = " + (a % b));
	}

}
