package com.GitHub_Updates;
//Operators are symbols that performs a particular task and 
//based on the type of operation operators are classified.

public class Operators_in_Java_Ternary_Operators {
	/*Ternary operators
 1) ?: 
	 */
	public static void main(String[] args) {
		int a=2;  
		int b=5;  
		int c=(a<b)?a:b; //condition checking a<b 2<5 is true. True mean it will print a value or else b value. 
		System.out.println(c);  
	}

}
